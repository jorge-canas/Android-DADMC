public class DatosEmpleado {
    int code;
    String name;
    String lastName;
    String birthday;
    String salary;

    public DatosEmpleado(int code, String name, String lastName, String birthday, String salary){
        this.code = code;
        this.name = name;
        this.lastName = lastName;
        this.birthday = birthday;
        this.salary = salary;
    }
}
